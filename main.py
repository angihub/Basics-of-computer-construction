def ffz(number):
    if abs(number) >= 1 and number < 0: number = number * 10 ** ((-1) * (len(str(int(number))) - 1))
    elif abs(number) >= 1 and number >= 0: number = number * 10 ** ((-1) * (len(str(int(number)))))
    return number

